import Foundation


// # 类和结构体 #


// Swift中的结构体和类有很多共同之处：
// 1、可定义属性
// 2、可定义方法
// 3、可定义下标脚本使用下标语法访问值
// 4、可定义初始化器
// 5、可以使用扩展来扩展当前结构体没有的功能
// 6、可以遵循协议

// 区别：

// 结构体所没有的功能
// 1、类可以使用继承
// 2、类型转换允许你在运行时检查和解释一个类实例的类型
// 3、反初始化器允许一个类实例释放任何所被分配的资源
// 4、引用计数

// 注意：结构体类型是通过值复制来传递，并且不会使用引用计数















